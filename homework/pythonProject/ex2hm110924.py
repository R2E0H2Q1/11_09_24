#2. Use a for loop and print:

# - All whole numbers from 0 to 10 inclusive:

for i in range(11):
    print(i, end=" ")
print()

# - All even numbers from 40 to 88 inclusive
for i in range(40, 89, 2):
    print(i, end=' ')
print()

# - All numbers divisible by 7 (without a remainder) from 77 to 105 inclusive

for i in range(77, 106, 7):
    print(i, end=' ')
print()

# - all the numbers divisible by 3 (without a remainder) from 99 to 66 (in descending order)

for i in range(99, 65, -3):
    print(i, end=' ')